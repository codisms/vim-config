
export class Whee {
  /**
   * Return the negative value of the passed value;
   *
   * @param x {number} Value to negate
   * @return {string} ...
   */
  negative(x) {
    return -x;
  }

  /**
   * Multiply two numbers.
   *
   * @param x {number} First number
   * @param y {number} Second number
   */
  multiply(x, y) {
    return x * y;
  }
}

